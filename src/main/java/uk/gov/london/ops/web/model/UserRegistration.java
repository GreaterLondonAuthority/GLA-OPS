package uk.gov.london.ops.web.model;

import javax.validation.constraints.NotNull;

public class UserRegistration {

    @NotNull(message = "Enter your email address")
    private String email;

    @NotNull(message = "Enter a password")
    private String password;

    @NotNull(message = "Enter your first name")
    private String firstName;

    @NotNull(message = "Enter your surname")
    private String lastName;

    @NotNull(message = "Enter your contact number")
    private String phoneNumber;

    @NotNull(message = "Enter your organisation ID or IMS number")
    private String orgCode;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getOrgCode() {
        return orgCode;
    }

    public void setOrgCode(String orgCode) {
        this.orgCode = orgCode;
    }
}
